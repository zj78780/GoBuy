package JavaBean;

public class Item {
	String id;
	int price;
	String name;
	String pic;
	String class1;
	String class2;
	
	public void setid(String id){
		this.id=id;
	}
	
	public String getid(){
		return this.id;
	}
	
	public void setprice(int price){
		this.price=price;
	}
	
	public int getprice(){
		return this.price;
	}
	
	public void setname(String name){
		this.name=name;
	}
	
	public String getname(){
		return this.name;
	}
	
	public void setpic(String pic ){
		this.pic=pic;
	}
	
	public String getpic(){
		return this.pic;
	}
	
	public void setclass1(String class1 ){
		this.class1=class1;
	}
	
	public String getclass1(){
		return this.class1;
	}
	
	public void setclass2(String class2 ){
		this.class2=class2;
	}
	
	public String getclass2(){
		return this.class2;
	}

}
